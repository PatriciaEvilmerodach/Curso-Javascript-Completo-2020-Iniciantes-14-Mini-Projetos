// Math é um objeto muito utilizado para realizar operações matemáticas. Uma lista detalhada de vários comandos possíveis pode ser encontrada em: https://developer.mozilla.org/pt-BR/docs/Web/JavaScript/Reference/Global_Objects/Math

// Alguns exemplos de métodos Math:

let exemplo1 = Math.random() // Retorna, por padrão, um número aleatório entre 0 e 1 toda vez que o comando é executado
console.log(exemplo1);

let exemplo2 = Math.max(44,63,16) // Retorna o maior número dentro de um array
console.log(exemplo2);

let exemplo3 = Math.min(1,11,55) // Retorna o menor número dentro de um array
console.log(exemplo3);