// Operadores de atribuição, como o nome sugere, atribuem um valor à uma variável

// Exemplo de operadores de Atribuição:

let exemplo1 = 100; // O operador = é o principal operador de atribuição; ele simplesmente atribui o valor à direita para a variável à esquerda
console.log(exemplo1);

let exemplo2 = 100;
exemplo2 += exemplo2 // O operador += é uma forma simplificada de somar dois valores e inserí-los à uma variável
console.log(exemplo2); // Aqui, basicamente aconteceu o seguinte: 100 + 100 e o resultado da soma (200) foi mostrado na tela

let exemplo3 = 100;
exemplo3 -= exemplo3; // O operador -= é uma forma simplificada de subtrair dois valores e inserí-los à uma variável
console.log(exemplo3); // Aqui, basicamente aconteceu o seguinte: 100 - 100 e o resultado da subtração (0) foi mostrado na tela

// O mesmo padrão exemplificado acima pode ser utilizado com os outros operadores aritméticos