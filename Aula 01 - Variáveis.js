// Basicamente uma Variável serve para receber um valor e utilizá-lo em algum lugar do nosso programa

let idade = 28;
// Este é um exemplo de variável sendo declarada, onde: "let" é o tipo, "idade" é o nome, "=" é o operador de atribuição, "28" é o valor atribuído à variável
console.log(idade);
// "console.log" é responsável por mostrar o valor na tela; a variável "idade" é o parâmetro declarado entre os parênteses

// Variáveis não podem ter seus nomes iniciando com números e nem espaços entre as palavras
// Uma boa prática para nomear variáveis é utilizar o "lowerCamelCase", ou seja: A primeira palavra inicia em minúscula e as palavras subsequentes em Maiúsculas
// Atenção ao nomes das variáveis no momento que precisarmos declara-las: Javascript é Case-Sensitive, logo a variável chamada "nome" é diferente da variável chamada "Nome"