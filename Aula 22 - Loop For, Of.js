// O Loop For/Of é uma forma mais simples de fazer interações com Arrays

let gridFormula1 = ['Ferrari', 'Mercedes', 'Red Bull', 'McLaren', 'Aston Martin', 'Alpha Tauri', 'Alfa Romeo', 'Alpine', 'Williams', 'Haas'];

for (let grid of gridFormula1){ // A declaração e a execução do comando é bem mais simplificada em comparação ao Loop For/In
    console.log(grid); // Aqui os itens do Array são listados na tela, cada um em uma linha diferente
}