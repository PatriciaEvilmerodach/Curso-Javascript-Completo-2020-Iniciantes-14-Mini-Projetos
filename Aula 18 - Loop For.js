// O Loop For serve para repetir o mesmo comando sem a necessidade de escrevê-lo várias vezes no código

// Exemplo: Temos uma variável "número" onde seu valor inicial é 1
// Vamos utilizar o operador de incremento (++) para aumentar o valor enquanto ele for menor ou igual à 10
// Quando o resultado do valor da variável for um número maior do que 10, o comando deverá parar de ser executado

for (let numero = 1; numero <= 10; numero++) { // O número inicia em 1; ele é menor ou igual à 10? Então acrescente +1 ao seu valor e assim sucessivamente
    console.log('Número: ', numero); // O console mostra o comando sendo executado em looping 10 vezes
}